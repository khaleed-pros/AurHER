
(function () {
    'use strict';

    document.addEventListener('DOMContentLoaded', function () {

        // ── Delete Product Confirmation ──
        var deleteForms = document.querySelectorAll('.delete-form');
        deleteForms.forEach(function (form) {
            form.addEventListener('submit', function (e) {
                e.preventDefault();
                if (confirm('Are you sure you want to delete this product? All variants and images will also be deleted. This cannot be undone.')) {
                    form.submit();
                }
            });
        });

        // ── Delete Variant Confirmation ──
        var deleteVariantForms = document.querySelectorAll('.delete-variant-form');
        deleteVariantForms.forEach(function (form) {
            form.addEventListener('submit', function (e) {
                e.preventDefault();
                if (confirm('Are you sure you want to delete this variant?')) {
                    form.submit();
                }
            });
        });

        // ── Delete Image Confirmation ──
        var deleteImageForms = document.querySelectorAll('.delete-image-form');
        deleteImageForms.forEach(function (form) {
            form.addEventListener('submit', function (e) {
                e.preventDefault();
                if (confirm('Are you sure you want to delete this image?')) {
                    form.submit();
                }
            });
        });

        // ── File Upload Preview ──
        var fileInput = document.getElementById('imageFileInput');
        var fileLabel = document.getElementById('fileLabel');

        if (fileInput && fileLabel) {
            fileInput.addEventListener('change', function () {
                if (this.files && this.files[0]) {
                    fileLabel.textContent = this.files[0].name;
                } else {
                    fileLabel.textContent = 'Choose image file...';
                }
            });
        }

        // ── Stock Badge Color ──
        var stockCells = document.querySelectorAll('[data-stock]');
        stockCells.forEach(function (cell) {
            var stock = parseInt(cell.getAttribute('data-stock'));
            var badge = cell.querySelector('.stock-badge');
            if (!badge) return;

            if (stock === 0) {
                badge.classList.add('out-of-stock');
                badge.textContent = 'Out of Stock';
            } else if (stock <= 5) {
                badge.classList.add('low-stock');
                badge.textContent = stock + ' left';
            } else {
                badge.classList.add('in-stock');
                badge.textContent = stock + ' in stock';
            }
        });

    });

})();
// ── SKU Auto Generator ──
var sizeInput = document.getElementById('sizeInput');
var colorInput = document.getElementById('colorInput');
var skuInput = document.getElementById('skuInput');

function generateSKU() {
    if (!sizeInput || !colorInput || !skuInput) return;
    if (skuInput.value.trim() !== '' && !skuInput.dataset.autoGenerated) return;

    var size = sizeInput.value.trim().toUpperCase().replace(/\s+/g, '').substring(0, 3);
    var color = colorInput.value.trim().toUpperCase().replace(/\s+/g, '').substring(0, 3);
    var random = Math.floor(1000 + Math.random() * 9000);

    if (size && color) {
        skuInput.value = color + '-' + size + '-' + random;
        skuInput.dataset.autoGenerated = 'true';
    }
}

if (sizeInput) sizeInput.addEventListener('change', generateSKU);
if (colorInput) colorInput.addEventListener('input', generateSKU);

// Allow manual override
if (skuInput) {
    skuInput.addEventListener('input', function () {
        delete skuInput.dataset.autoGenerated;
    });
}
